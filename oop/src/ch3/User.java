package ch3;

public class User {
	String ad;
	String sıyad;
	int numara;
	int yas;
	boolean aktiflik;
	
	public String getAd() {
		return ad;
	}
	public void setAd(String ad) {
		this.ad = ad;
	}
	
	public String getSıyad() {
		return sıyad;
	}
	public void setSıyad(String sıyad) {
		this.sıyad = sıyad;
	}
	
	public int getNumara() {
		return numara;
	}
	public void setNumara(int numara) {
		this.numara = numara;
	}
	
	public int getYas() {
		return yas;
	}
	public void setYas(int yas) {
		this.yas = yas;
	}
	
	public boolean isAktiflik() {
		return aktiflik;
	}
	public void setAktiflik(boolean aktiflik) {
		this.aktiflik = aktiflik;
	}
	
	
}
